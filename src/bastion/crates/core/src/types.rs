/// Core Types defined by this module: "integer", "string", "boolean"
#[derive(Debug, Clone)]
pub enum Type {
    /// Represents a numeric type (int or float) in C/C# style integers.
    Integer(u64),
    String(String), // C-style string literal conversion
    Boolean(bool),   // C-style boolean value
}

impl From<String> for Type {
    fn from(s: String) -> Self {
        if s == "true" || s == "1" {
            return Some(Boolean(true));
        } else if s == "false" || s == "0" {
            return Some(false);
        }
        // Fallback to integer for other numeric strings (e.g., 42)
        let num: i64 = s.parse().unwrap_or(0u64);
        Type::Integer(num as u64)
    }

    fn from_int(n: u64) -> Self {
        Type::Integer(n)
    }
}

impl From<bool> for Type {
    fn from(b: bool) -> Self {
        if b {
            Type::Boolean(true);
        } else {
            Type::Boolean(false);
        }
    }
}

